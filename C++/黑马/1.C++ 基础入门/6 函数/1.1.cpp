//            1 函数的定义
#include <iostream>
using namespace std;

/*  实现一个加法函数，功能是，传入两个整型数据，计算数据相加的结果，并且返回。
    1. 返回值类型：         int
    2. 函数名：             add
    3. 参数列表             (int num1, int num2)
    4. 函数体语句：         int sum = num1 + num2
    5. return 表达式：      return sum
*/
int add (int num1, int num2)
    {
        int sum = num1 + num2;
        
        return sum;
    }


    

