/*
**练习案例1**：五只小猪称体重

**案例描述：**

在一个数组中记录了五只小猪的体重，如：int arr[5] = {300,350,200,400,250};

找出并打印最重的小猪体重。
*/
#include <iostream>
using namespace std;

int main()
{
    // 1.创建5只小猪体重的数组
    int arr[5]={300,350,200,400,250};  
    // 2. 从数组中找到最大值
    int max=0;  // 先认定一个最大值为0

    for(int i=0;i<5;i++)
    {
        // 如果访问的数组中的元素比我认定的最大值还要大，更新最大值
        if(arr[i]>max)
        {
            max=arr[i];
        }
    }
    // 打印最大值
    cout << max << endl;
    return 0;
}